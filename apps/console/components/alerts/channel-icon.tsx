// Lucide fallbacks (no open brand mark) + their tint when active.
const GLYPH: Partial<
	Record<AlertChannelType, { Icon: typeof Mail; color?: string }>
> = {
	webhook: { Icon: Webhook },
	email: { Icon: Mail },
	teams: { Icon: MessagesSquare, color: "#6264A7" },
};

interface ChannelIconProps {
	type: AlertChannelType;
	size?: number;
	/** Full-colour when true; grayscale/muted otherwise. Defaults to true. */
	active?: boolean;
	className?: string;
}

/** Transport brand icon, grayscale when inactive. */
export function ChannelIcon({
	type,
	size = 20,
	active = true,
	className,
}: ChannelIconProps) {
	if (type === "slack") {
		return (
			<svg
				viewBox="0 0 122.8 122.8"
				width={size}
				height={size}
				className={cn("shrink-0", !active && "grayscale opacity-90", className)}
				aria-hidden="true"
			>
				<path
					fill="#E01E5A"
					d="M25.8 77.6c0 7.1-5.8 12.9-12.9 12.9S0 84.7 0 77.6s5.8-12.9 12.9-12.9h12.9v12.9zm6.5 0c0-7.1 5.8-12.9 12.9-12.9s12.9 5.8 12.9 12.9v32.3c0 7.1-5.8 12.9-12.9 12.9s-12.9-5.8-12.9-12.9V77.6z"
				/>
				<path
					fill="#36C5F0"
					d="M45.2 25.8c-7.1 0-12.9-5.8-12.9-12.9S38.1 0 45.2 0s12.9 5.8 12.9 12.9v12.9H45.2zm0 6.5c7.1 0 12.9 5.8 12.9 12.9s-5.8 12.9-12.9 12.9H12.9C5.8 58.1 0 52.3 0 45.2s5.8-12.9 12.9-12.9h32.3z"
				/>
				<path
					fill="#2EB67D"
					d="M97 45.2c0-7.1 5.8-12.9 12.9-12.9s12.9 5.8 12.9 12.9-5.8 12.9-12.9 12.9H97V45.2zm-6.5 0c0 7.1-5.8 12.9-12.9 12.9s-12.9-5.8-12.9-12.9V12.9C64.7 5.8 70.5 0 77.6 0s12.9 5.8 12.9 12.9v32.3z"
				/>
				<path
					fill="#ECB22E"
					d="M77.6 97c7.1 0 12.9 5.8 12.9 12.9s-5.8 12.9-12.9 12.9-12.9-5.8-12.9-12.9V97h12.9zm0-6.5c-7.1 0-12.9-5.8-12.9-12.9s5.8-12.9 12.9-12.9h32.3c7.1 0 12.9 5.8 12.9 12.9s-5.8 12.9-12.9 12.9H77.6z"
				/>
			</svg>
		);
	}

	const brand = BRAND[type];
	if (brand) {
		return (
			<svg
				viewBox={brand.viewBox}
				width={size}
				height={size}
				fill="currentColor"
				className={cn("shrink-0", !active && "text-muted-foreground", className)}
				style={active ? { color: brand.color } : undefined}
				aria-hidden="true"
			>
				<path d={brand.d} />
			</svg>
		);
	}

	const glyph = GLYPH[type] ?? { Icon: Webhook };
	const Glyph = glyph.Icon;
	return (
		<Glyph
			size={size}
			className={cn(
				"shrink-0",
				!active && "text-muted-foreground",
				active && !glyph.color && "text-foreground",
				className,
			)}
			style={active && glyph.color ? { color: glyph.color } : undefined}
			aria-hidden="true"
		/>
	);
}
