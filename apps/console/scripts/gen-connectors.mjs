// Codegen for the pluggable-connectors SSOT. Reads the single manifest
// (packages/core/categories/catalog.json) and emits:
//   1. lib/connectors/registry.generated.ts — typed provider/credential/form
//      metadata that drives the connector credential form + the Project provider
//      selectors (no per-provider hand-coded components).
//   2. lib/db/seed/connectors.generated.sql — idempotent catalog rows applied by
//      the migrate one-shot (scripts/migrate.mjs).
// The same manifest is consumed by Go (go:embed, packages/core/categories) so a
// provider is declared exactly once. Run: `pnpm -F console gen:connectors`.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const manifestPath = join(here, "../../../packages/core/categories/catalog.json");
const registryOut = join(here, "../lib/connectors/registry.generated.ts");
const seedOut = join(here, "../lib/db/seed/connectors.generated.sql");

const HEADER_TS = `// SPDX-FileCopyrightText: 2026 Alethia Labs <legal@alethialabs.io>
// SPDX-License-Identifier: AGPL-3.0-only

// GENERATED by scripts/gen-connectors.mjs from packages/core/categories/catalog.json.
// Do NOT edit by hand — edit the manifest and run \`pnpm -F console gen:connectors\`.
`;

const HEADER_SQL = `-- SPDX-FileCopyrightText: 2026 Alethia Labs <legal@alethialabs.io>
-- SPDX-License-Identifier: AGPL-3.0-only
-- GENERATED by scripts/gen-connectors.mjs from packages/core/categories/catalog.json.
-- Idempotent seed of the pluggable-connector catalog rows.
`;

/** Escapes a JS string literal value (single-quoted). */
function js(value) {
	return value === undefined || value === null
		? "undefined"
		: JSON.stringify(value);
}

/** Escapes a string for a single-quoted SQL literal. */
function sqlLiteral(value) {
	if (value === undefined || value === null) return "NULL";
	return `'${String(value).replace(/'/g, "''")}'`;
}

const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
const providers = manifest.providers ?? [];
// Built-in cloud/git connectors are catalog rows only (no module/credential
// schema): seeded into the `connectors` table but NOT emitted to the TS registry.
const builtin = manifest.builtin ?? [];
// Full catalog (all rows) for the SQL seed; providers alone drive the TS registry.
const catalogRows = [...builtin, ...providers];

// ── registry.generated.ts ─────────────────────────────────────────────────────
const ts = `${HEADER_TS}
/** Categories that support pluggable (non-cloud-native) connector backends. */
export const PLUGGABLE_CATEGORIES = ["dns", "secrets", "registry", "observability"] as const;
export type PluggableCategory = (typeof PLUGGABLE_CATEGORIES)[number];

/** A single field collected for a credential or a component's provider_config. */
export interface ConnectorField {
	key: string;
	label: string;
	type: "text" | "password" | "boolean" | "select";
	secret?: boolean;
	required?: boolean;
	help?: string;
	default?: string | boolean;
	options?: string[];
}

export interface ConnectorProviderMeta {
	category: PluggableCategory;
	slug: string;
	name: string;
	description: string;
	organization: string;
	icon_url: string;
	docs_url?: string;
	status: "active" | "coming_soon";
	sort_order: number;
	modulePath: string;
	credentialFields: ConnectorField[];
	providerConfigFields: ConnectorField[];
}

export const CONNECTOR_PROVIDERS: ConnectorProviderMeta[] = [
${providers
	.map((p) => {
		const cred = p.credential?.fields ?? [];
		const cfg = p.provider_config?.fields ?? [];
		return `	{
		category: ${js(p.category)},
		slug: ${js(p.slug)},
		name: ${js(p.name)},
		description: ${js(p.description)},
		organization: ${js(p.organization)},
		icon_url: ${js(p.icon_url)},
		docs_url: ${js(p.docs_url)},
		status: ${js(p.status)},
		sort_order: ${p.sort_order},
		modulePath: ${js(p.module_path)},
		credentialFields: ${JSON.stringify(cred)},
		providerConfigFields: ${JSON.stringify(cfg)},
	},`;
	})
	.join("\n")}
];

/** Pluggable providers offered for a category (excludes the cloud-native default). */
export function getProvidersForCategory(category: PluggableCategory): ConnectorProviderMeta[] {
	return CONNECTOR_PROVIDERS.filter((p) => p.category === category);
}

/** Look up a provider by (category, slug), or undefined for the native default. */
export function getConnectorProvider(
	category: string,
	slug: string | null | undefined,
): ConnectorProviderMeta | undefined {
	if (!slug || slug === "native") return undefined;
	return CONNECTOR_PROVIDERS.find((p) => p.category === category && p.slug === slug);
}

/** Look up a provider by slug alone (slugs are globally unique in the catalog). */
export function getConnectorProviderBySlug(slug: string): ConnectorProviderMeta | undefined {
	return CONNECTOR_PROVIDERS.find((p) => p.slug === slug);
}
`;

mkdirSync(dirname(registryOut), { recursive: true });
writeFileSync(registryOut, ts);

// ── connectors.generated.sql ──────────────────────────────────────────────────
const rows = catalogRows
	.map((p) => {
		const cols = [
			sqlLiteral(p.slug),
			sqlLiteral(p.name),
			sqlLiteral(p.description),
			`${sqlLiteral(p.category)}::connector_category`,
			`${sqlLiteral(p.auth_method)}::connector_auth_method`,
			sqlLiteral(p.organization),
			sqlLiteral(p.icon_url),
			sqlLiteral(p.docs_url),
			`${sqlLiteral(p.status)}::connector_status`,
			String(p.sort_order),
		];
		return `  (${cols.join(", ")})`;
	})
	.join(",\n");

const sqlText = `${HEADER_SQL}
INSERT INTO connectors (slug, name, description, category, auth_method, organization, icon_url, docs_url, status, sort_order)
VALUES
${rows}
ON CONFLICT (slug) DO UPDATE SET
  name = EXCLUDED.name,
  description = EXCLUDED.description,
  category = EXCLUDED.category,
  auth_method = EXCLUDED.auth_method,
  organization = EXCLUDED.organization,
  icon_url = EXCLUDED.icon_url,
  docs_url = EXCLUDED.docs_url,
  status = EXCLUDED.status,
  sort_order = EXCLUDED.sort_order,
  updated_at = now();
`;

mkdirSync(dirname(seedOut), { recursive: true });
writeFileSync(seedOut, sqlText);

console.log(
	`✓ generated registry (${providers.length} pluggable) + seed (${catalogRows.length} connectors) → lib/connectors/registry.generated.ts + lib/db/seed/connectors.generated.sql`,
);
